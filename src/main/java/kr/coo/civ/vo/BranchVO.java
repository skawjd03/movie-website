package kr.coo.civ.vo;

public class BranchVO {
	private int br_no;
	private String br_loc;
	private String br_name;
	private String br_tel;
	
	public int getBr_no() {
		return br_no;
	}
	public void setBr_no(int br_no) {
		this.br_no = br_no;
	}
	public String getBr_loc() {
		return br_loc;
	}
	public void setBr_loc(String br_loc) {
		this.br_loc = br_loc;
	}
	public String getBr_name() {
		return br_name;
	}
	public void setBr_name(String br_name) {
		this.br_name = br_name;
	}
	public String getBr_tel() {
		return br_tel;
	}
	public void setBr_tel(String br_tel) {
		this.br_tel = br_tel;
	}
	
}
